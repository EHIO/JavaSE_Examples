package structural.facade.hometheater;

/**
 * 爆米花机
 *
 * @author wang.gang
 */
public class PopcornPopper {

    public void on() {
        System.out.println("打开爆米花机");
    }

    public void pop() {
        System.out.println("开始生产爆米花");
    }

    public void off() {
        System.out.println("关闭爆米花机");
    }

}
